                    <p class="answer">Para que el crecimiento, el desarrollo y la salud sean óptimos, hay que alimentar a los lactantes exclusivamente con leche materna durante los seis primeros meses de vida. Por «lactancia materna exclusiva» se entiende no proporcionar al lactante ningún alimento ni bebida (ni siquiera agua) que no sea la leche materna. Se puede, no obstante, darle gotas o jarabes (vitaminas, minerales y medicamentos). La leche materna es el alimento idóneo para el crecimiento y el desarrollo sano del bebé; además, la lactancia materna forma parte del proceso reproductivo, y tiene importantes repercusiones para la salud de las madres.</p>
